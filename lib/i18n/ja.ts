export function jaStatus(value: unknown) {
  const key = String(value ?? "unknown");
  return ja.status[key as keyof typeof ja.status] ?? key;
}

export function jaCategory(value: unknown) {
  const key = String(value ?? "Unknown");
  return ja.category[key as keyof typeof ja.category] ?? (key === "Unknown" ? "未分類" : key);
}

export function jaSceneTag(value: unknown) {
  const key = String(value ?? "");
  return ja.sceneTag[key as keyof typeof ja.sceneTag] ?? jaDisplay(value);
}

export function jaCategoryTag(value: unknown) {
  const key = String(value ?? "");
  return ja.categoryTag[key as keyof typeof ja.categoryTag] ?? jaDisplay(value);
}

export function jaDisplay(value: unknown) {
  const key = String(value ?? "Unknown");
  if (!key || key === "null" || key === "undefined" || key === "Unknown" || key === "unknown") return "未分類";
  return jaStatus(key);
}

export function jaGooglePlaceType(value: unknown) {
  const key = String(value ?? "");
  if (!key || key === "null" || key === "undefined" || key === "Unknown" || key === "unknown") return "未分類";
  return ja.googlePlaceType[key as keyof typeof ja.googlePlaceType] ?? key.replaceAll("_", " ");
}

const LOW_INFORMATION_GOOGLE_TYPES = new Set([
  "point_of_interest",
  "establishment",
  "store",
  "premise",
  "general_contractor",
  "business",
  "facility",
  "spot"
]);

export function jaGooglePlaceTypes(primary: unknown, types: unknown) {
  const rawTypes = Array.isArray(types) ? types.map(String) : [];
  const ordered = [primary ? String(primary) : "", ...rawTypes]
    .filter(Boolean)
    .filter((type, index, array) => array.indexOf(type) === index)
    .filter((type) => !LOW_INFORMATION_GOOGLE_TYPES.has(type));
  return ordered.slice(0, 4).map(jaGooglePlaceType).join(" / ") || null;
}
